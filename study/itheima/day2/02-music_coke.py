# 可乐与牛奶调换

coke = "可乐"
music = "牛奶"
desk = ""
print(f"{coke}，{music}")
# 先把可乐拿走放桌子上
desk = coke
# 再把牛奶放在可乐的位置上
coke = music
# 继续把放在桌子上的可乐放在牛奶的位置
music = desk
print(f"{coke}，{music}")

